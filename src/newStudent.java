    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String studentID=jTextField1.getText();
        String name=jTextField2.getText();
        String program=(String)jComboBox1.getSelectedItem();
        String courseName=(String)jComboBox2.getSelectedItem();
        
        try{
            Connection conn=connectionProvider.getConn();
            Statement st=conn.createStatement();
            st.executeUpdate("insert into newstudenttable values('"+studentID+"','"+name+"','"+program+"','"+courseName+"')");
            JOptionPane.showMessageDialog(null,"Succussfully updated");
            setVisible(false);
            new newStudent().setVisible(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Student ID already exist");
            setVisible(false);
            new newStudent().setVisible(true);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

